// Premitive types : String, Number, Boolean, Undefined, Null
let myname = "Mohan"; // String Literal
let age = 25; // Number Literal
let isMarried = false; // Boolean Literal
console.log(myname, age, isMarried);
console.log(typeof myname, typeof age, typeof isMarried);
let a = null; // Null
let b = undefined; // Undefined
console.log(typeof a, typeof b);